    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (jTextField2.getText().equals("") || jTextField3.getText().equals("") || jTextField4.getText().equals("") || jTextField5.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Coloque os dados", "Erro", JOptionPane.ERROR_MESSAGE);

        } else {

            try {
                dao.insertCustomer(new Customer(Integer.parseInt(jTextField5.getText()), jTextField1.getText(), jTextField2.getText(), jTextField3.getText(), Integer.parseInt(jTextField4.getText()), buttonGroup1.getButtonCount()));
            } catch (SQLException ex) {
                Logger.getLogger(AdicionarUI.class.getName()).log(Level.SEVERE, null, ex);
            }

        }

        SakilaUI s;
        try {
            s = new SakilaUI();
            s.setVisible(true);
            setVisible(false);
        } catch (SQLException ex) {
            Logger.getLogger(AdicionarUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

